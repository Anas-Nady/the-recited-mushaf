// Helper to get order, handling normalization if needed
export const getSurahOrder = (surahName: string): number => {
  // Try exact match
  if (SURAH_ORDER[surahName]) return SURAH_ORDER[surahName];

  // Try normalized match (remove "سورة " prefix)
  const nameOnly = surahName.replace("سورة ", "").trim();
  const found = Object.entries(SURAH_ORDER).find(([key]) =>
    key.includes(nameOnly)
  );
  return found ? found[1] : 999; // 999 for unknown, puts them at the end
};
